# Equal to (==)
print (5 == 5) # True
print (5 == 3) # False

# Not equal to (!=)
print (5 != 3) # True
print (5 != 5) # False

# Greater than ( >)
print (5 > 3) # True
print (3 > 5) # False

# Less than ( <)
print (3 < 5) # True
print (5 < 3) # False

# Greater than or equal ( >=)
print (5 >= 5) # True
print (5 >= 3) # True
print (3 >= 5) # False

# Less than or equal ( <=)
print (3 <= 5) # True
print (5 <= 5) # True
print (5 <= 3) # False

age = 20
score = 85

# Compare variables to values
print ( age >= 18) # True
print ( score < 90) # True

# Compare variables to each other
x = 10
y = 20
print ( x < y ) # True
print ( x == y ) # False

# Use in real scenarios
passing_score = 60
